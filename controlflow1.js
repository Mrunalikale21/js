const month = 3

switch(month){
    case 1:
        console.log("january");
        break;
       
    case 2:
        console.log("feb");
        break;
       
    case 3:
        console.log("march");
        break;

    case 4:
        console.log("april");
        break;

        default:
            console.log("defult case match");
            break;
       
}

//if we comment out break then where case matches code after it will run except default condition so it is important to write break condition
//break will break control flow

